package my_program;

public class changeLanguage {
    int id;
    String eng;
    String ua;
    String ru;

    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }

    public String getEng() {
        return eng;
    }
    public void setEng(String eng) {
        this.eng = eng;
    }

    public String getUa() {
        return ua;
    }
    public void setUa(String ua) {
        this.ua = ua;
    }

    public String getRu() {
        return ru;
    }
    public void setRu(String ru) {
        this.ru = ru;
    }


}
